void	fill_map(t_info *info, int worldmap[24][24])
{
	if (info == NULL)
		return ;
	info->map = ft_calloc(info->map_height, sizeof(int *));
	for (int i = 0; i < info->map_height; i++)
		info->map[i] = ft_calloc(info->map_width, sizeof(int));
	for (int i = 0; i < info->map_height; i++)
		for (int j = 0; j < info->map_width; j++)
			info->map[i][j] = worldmap[i][j];
}

